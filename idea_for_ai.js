export default function OrderProcessor() {
    const [input, setInput] = useState("");
    const [response, setResponse] = useState(null);
    const [loading, setLoading] = useState(false);
    const [error, setError] = useState(null);

    const handleSubmit = async () => {
        if (!input.trim()) return;

        setLoading(true);
        setError(null);
        setResponse(null);

        try {
            const res = await fetch("https://openrouter.ai/api/v1/chat/completions", {
                method: "POST",
                headers: {
                    "Authorization": `Bearer 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`,
                    "Content-Type": "application/json",
                    "HTTP-Referer": window.location.origin,
                    "X-Title": "Order Processor"
                },
                body: JSON.stringify({
                    model: "openai/gpt-5-mini",
                    temperature: 0,
                    messages: [
                        {
                            role: "system",
                            content: SYSTEM_PROMPT
                        },
                        {
                            role: "user",
                            content: input
                        }
                    ]
                })
            });

            const data = await res.json();

            if (!res.ok) {
                throw new Error(data.error?.message || "Request failed");
            }

            const content = data.choices[0].message.content;

            // GPT должен вернуть чистый JSON
            const parsed = JSON.parse(content);
            setResponse(parsed);

        } catch (err) {
            setError(err.message);
        } finally {
            setLoading(false);
        }
    };

    return (
        <div style={{ maxWidth: 600, margin: "40px auto", fontFamily: "sans-serif" }}>
            <h2>Order Processor</h2>

            <textarea
                value={input}
                onChange={(e) => setInput(e.target.value)}
                placeholder="Введите заказ..."
                rows={6}
                style={{ width: "100%", marginBottom: 12 }}
            />

            <button onClick={handleSubmit} disabled={loading}>
                {loading ? "Обработка..." : "Отправить"}
            </button>

            {error && (
                <div style={{ marginTop: 20, color: "red" }}>
                    Ошибка: {error}
                </div>
            )}

            {response && (
                <pre style={{ marginTop: 20, background: "#f4f4f4", padding: 12 }}>
          {JSON.stringify(response, null, 2)}
        </pre>
            )}
        </div>
    );
}
